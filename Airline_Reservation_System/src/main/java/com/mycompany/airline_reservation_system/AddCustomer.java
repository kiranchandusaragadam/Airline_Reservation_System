    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        try {
            // TODO add your handling code here:
            // get all data from user
            String cId = customerId.getText();
            String fn = firstName.getText();
            String ln = lastName.getText();
            String gen = "";
            if(maleRadio.isSelected()){
                gen = "Male";
            }
            else{
                gen = "Female";
            }
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String dob = df.format(date.getDate());
            String addr = address.getText();
            String contact = this.contact.getText();
            String psprt = passport.getText();
            String nationalId = nationalID.getText();
            
            Class.forName("com.mysql.jdbc.Driver");
            // create connection with our database
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project", "root", "Skc@1999");
            // prepare statement
            String sql = "insert into customers (CustomerID, FirstName, LastName, Gender, DOB, Address, Contact, PassportNo, NationalID) values(?,?,?,?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, cId);
            pst.setString(2, fn);
            pst.setString(3, ln);
            pst.setString(4, gen);
            pst.setString(5, dob);
            pst.setString(6, addr);
            pst.setString(7, contact);
            pst.setString(8, psprt);
            pst.setString(9, nationalId);  
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Customer details added successfully!");
            
            this.setVisible(false);
            // close connection
            con.close();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addBtnActionPerformed

