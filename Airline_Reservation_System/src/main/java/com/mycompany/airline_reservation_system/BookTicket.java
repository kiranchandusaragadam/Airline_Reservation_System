    private void booktcktBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_booktcktBtnActionPerformed
        try {
            // TODO add your handling code here:
            // get all details required for tickets table in database
            String tId = tktId.getText();
            String cId = customerId.getText();
            String fn = firstName.getText();
            String ln = lastName.getText();
            String gen = "";
            if(maleRadio.isSelected()){
                gen = "Male";
            }
            else{
                gen = "Female";
            }
            String cnt = contact.getText();
            String dep = fromCombo.getSelectedItem().toString();
            String arr = toCombo.getSelectedItem().toString();
            
            if(cId.equals("") || dep.equals(arr)){
                JOptionPane.showMessageDialog(this, "Please fill all required fields!");
            }
            else{
            Class.forName("com.mysql.jdbc.Driver");
            // create connection with our database
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project", "root", "Skc@1999");
            // prepare statement
            String sql = "insert into tickets (TicketID, FlightID, CustomerID, FirstName, LastName, Gender, Contact, Departure, Arrival) values (?, ?, ?, ?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, tId);
            pst.setString(2, bookedFlightId);
            pst.setString(3, cId);
            pst.setString(4, fn);
            pst.setString(5, ln);
            pst.setString(6, gen);
            pst.setString(7, cnt);
            pst.setString(8, dep);
            pst.setString(9, arr);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Flight booked successfully!");
            this.setVisible(false);
            con.close();
            }
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BookTicket.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(BookTicket.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_booktcktBtnActionPerformed

