    private void searchFlightsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchFlightsActionPerformed
        try {
            // TODO add your handling code here:
            // search flights based on source and destination points
            String src = fromCombo.getSelectedItem().toString();
            String dst = toCombo.getSelectedItem().toString();
            
            Class.forName("com.mysql.jdbc.Driver");
            // create connection with our database
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project", "root", "Skc@1999");
            // prepare statement
            String sql = "select * from flights where Departure = ? and Arrival = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, src);
            pst.setString(2, dst);
            ResultSet rs = pst.executeQuery();
            
            // create default table model to add data to our Jtable
            DefaultTableModel dtm = (DefaultTableModel) table.getModel();
            dtm.setRowCount(0);
            
            while(rs.next()){
                Vector v = new Vector();
                v.add(rs.getString("FlightID"));
                v.add(rs.getString("FlightName"));
                v.add(rs.getString("Departure"));
                v.add(rs.getString("Arrival"));
                v.add(rs.getString("Duration"));
                v.add(rs.getString("Date"));
                
                dtm.addRow(v);
            }
            con.close();
            
        } catch (SQLException ex) {
            Logger.getLogger(BookTicket.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(BookTicket.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchFlightsActionPerformed

    private void searchCustActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchCustActionPerformed
        // TODO add your handling code here:
        try {
            // TODO add your handling code here:
            // get customer id from user to get details of that customer if present
            String custId = customerId.getText();
            
            Class.forName("com.mysql.jdbc.Driver");
            // create connection with our database
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project", "root", "Skc@1999");
            // prepare statement
            String sql = "select * from customers where CustomerID = ?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, custId);
            
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                firstName.setText(rs.getString("FirstName"));
                lastName.setText(rs.getString("LastName"));
                if(rs.getString("Gender").equals("Male")){
                    maleRadio.setSelected(true);
                }
                else{
                    femaleRadio.setSelected(true);
                }
                contact.setText(rs.getString("Contact"));
            }
            else{
                JOptionPane.showMessageDialog(this, "Searching customer details not exist!");
            }
     
            con.close();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(SearchCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_searchCustActionPerformed

