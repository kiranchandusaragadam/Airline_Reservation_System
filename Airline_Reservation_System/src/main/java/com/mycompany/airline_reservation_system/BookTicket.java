    public void autoID(){
        try {
            // connection to database through JDBC
            // register driver class
            Class.forName("com.mysql.jdbc.Driver");
            // create connection with our database
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project", "root", "Skc@1999");
            // prepare statement
            Statement st = con.createStatement();
            // execute query
            ResultSet rs = st.executeQuery("select MAX(TicketID) from tickets");
            rs.next(); // initialization
            String tId = rs.getString("MAX(TicketID)");
            if(tId == null){
                tktId.setText("TK001");
            }
            else{
                int id = Integer.parseInt(tId.substring(2));
                id++;
                tktId.setText("TK"+String.format("%03d", id));
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
