    private void addFlightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFlightActionPerformed
        try {
            // TODO add your handling code here:
            // get all data from user
            String fId = flightId.getText();
            String fn = flightName.getText();
            String dep = departure.getText();
            String arr = arrival.getText();
            String dur = duration.getText();
            String seats = this.seats.getText();
            String fare = this.fare.getText();
            DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String dt = df.format(date.getDate());
            
            Class.forName("com.mysql.jdbc.Driver");
            // create connection with our database
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/airline_project", "root", "Skc@1999");
            // prepare statement
            String sql = "insert into flights (FlightID, FlightName, Departure, Arrival, Duration, Seats, Fare, Date) values(?,?,?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, fId);
            pst.setString(2, fn);
            pst.setString(3, dep);
            pst.setString(4, arr);
            pst.setString(5, dur);
            pst.setString(6, seats);
            pst.setString(7, fare);
            pst.setString(8, dt);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Flight added successfully!");
            this.setVisible(false);
            con.close();
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddFlight.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_addFlightActionPerformed

